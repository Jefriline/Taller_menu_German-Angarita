// Función para convertir de segundos a minutos
export function stomin(segundos) {
    return segundos / 60;
}

// Función para convertir de minutos a horas
export function mtohoras(minutos) {
    return minutos / 60;
}

// Función para convertir de horas a días
export function htodias(horas) {
    return horas / 24;
}

// Función para convertir de días a semanas
export function dtosemanas(dias) {
    return dias / 7;
}

// Función para convertir de semanas a años
export function stoanios(semanas) {
    return semanas / 52;
}
