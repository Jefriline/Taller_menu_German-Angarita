
//Función que convierte de metros a Kilómetros
export function mtokm(metros) {
    return (metros / 1000);
}

//Función que convierte de decímetro a metro.
export function dmtom(decimetro) {
    return (decimetro / 10);
}


// Función para convertir de metros a centímetros
export function mtocm(metro) {
    return (metro * 100);
}


// Función para convertir de hectometro a metros
export function hmtom(hectometro) {
    return (hectometro * 100);
}


// Función para decametro a metro
export function damtom(decametro) {
    return (decametro * 10);
}